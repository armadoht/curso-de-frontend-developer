/**
5 Tips para aprender CSS

Css es un lenguaje que puede resultar complicado, pero los
siguientes tips te ayudarán a conocer y dominarlo.

1.- Identificar los diferentes selectores que puedes utilizar
para estilizar tu HTML.

Universal
*{
    margin:0;
}

Tipo
h1{
    color:red;
}

Clase
.saludo{
    font-size:2em;
}

ID
#id{
    border-radius:20px;
}

2.- Conoce la anatomía de su declaración, es decir, 
la estructura base con la que debe escribirse.

Selector
p{
    Propiedad
    color: red;
            Valor
}
---Declaración---


3.- Comprende qué es el modelo de caja y qué propiedades
afectan su dimensionamiento.

-----------------
-margin         -
-Border        -
-Padding      -
-p        -
-----------------

4.- Domina cómo se posicionan los elementos. Usando 
posicionamientos:
-Absoluto
-Relativo
-Flexbox
-Css Grid.

5.- Aprende haciendo

La práctica y la constancia son fundamentales en tu proceso de
aprendizaje.


 */