/**
Anatomía de un Elemento HTML: Atributos, Anidamiento y Elementos vacíos
Nuestros elementos HTML se componen de:

Etiqueta de apertura: el nombre de nuestra etiqueta 
encerrado entre símbolos de mayor o menor. Por ejemplo: <h1>.

Contenido: dentro de nuestras etiquetas podemos añadir texto u 
otros elementos HTML, lo que conocemos como anidamiento. Etiqueta de cierre: 
son casi iguales que las etiquetas de apertura, pero también necesitan 
un slash (/) antes del nombre de la etiqueta. Por ejemplo: </h1>.
Las etiquetas de apertura también pueden tener atributos. 
Los atributos nos permiten definir características especiales 
para nuestros elementos: <etiqueta atributo=""valor del atributo"">. 
Por ejemplo: <h1 class=""saludo"">.

También existen elementos vacíos. 
Estos elementos no tienen contenido ni etiqueta de cierre, 
solo etiqueta de apertura y atributos. 
Por ejemplo: <img src="puppy.png alt="mi mascota>.
 */